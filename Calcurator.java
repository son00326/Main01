package com.javalec.function;

public class Calcurator {

	
	
	//Field and property 
	int i = 0; 
	int k = 0;
	
	public Calcurator(int i, int k) {
		super();
		this.i = i;
		this.k = k;
	}
	
	
	//Constructor
	public Calcurator() {
		
	}
	
	//method 
	public void calc() { 
		int sum = i + k  ; 
		System.out.println("덧셈 : " + sum);
	}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
